
public class ShoppingCart {
	private String addtoCart;
	private String removefromCart;
	private String getCart;
	private String confirm;
	public String getAddtoCart() {
		return addtoCart;
	}
	public void setAddtoCart(String addtoCart) {
		this.addtoCart = addtoCart;
	}
	public String getRemovefromCart() {
		return removefromCart;
	}
	public void setRemovefromCart(String removefromCart) {
		this.removefromCart = removefromCart;
	}
	public String getGetCart() {
		return getCart;
	}
	public void setGetCart(String getCart) {
		this.getCart = getCart;
	}
	public String getConfirm() {
		return confirm;
	}
	public void setConfirm(String confirm) {
		this.confirm = confirm;
	}
}